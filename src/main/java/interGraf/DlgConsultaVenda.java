    private void botaoExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoExcluirActionPerformed
        venSelecionado = pegarLinhaSelecionada();
        if ( venSelecionado != null) {
            if ( JOptionPane.showConfirmDialog(this, "Deseja excluir a VENDA do cliente?" +
                    venSelecionado.getCliente().getNome(), "Confirmar", JOptionPane.YES_NO_OPTION  ) == JOptionPane.OK_OPTION  ) {
                try {
                    gerIg.getGerDominio().excluir(venSelecionado);
                    excluirLinhaSelecionada();
                    JOptionPane.showMessageDialog(this, "Venda excluída com sucesso.");
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "ERRO ao excluir CLIENTE.");
                    Logger.getLogger(DlgConsultaVenda.class.getName()).log(Level.SEVERE, null, ex);
                }
            } 
        } 
    }//GEN-LAST:event_botaoExcluirActionPerformed

    private void botaoProdutosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoProdutosActionPerformed
        venSelecionado = pegarLinhaSelecionada();
        if ( venSelecionado != null ) {
            String msg = "";
            for (ItemVenda iv : venSelecionado.getItensVenda()  ) {
                String nome = iv.getChComposta().getProduto().getNome() + " -  " 
                            + iv.getChComposta().getProduto().getUnidade();
                msg = msg.concat(nome).concat("\n");
            }
            JOptionPane.showMessageDialog(this, msg);
        }
    }//GEN-LAST:event_botaoProdutosActionPerformed

    private void relatoriosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_relatoriosActionPerformed
        // GERAR COM O TIPO DE BUSCAR "CLIENTE"
        try {
            List<Venda> lista = gerIg.getGerDominio().pesquisarVenda(cmbTipo.getSelectedIndex(), txtPesquisa.getText() ) ;
            gerIg.getGerRelatorios().relComLista(lista, "relVendas.jasper");
        } catch ( HibernateException ex) {
            JOptionPane.showMessageDialog(this, "Erro ao pesquisar pedido." + ex);
        } 
    }//GEN-LAST:event_relatoriosActionPerformed
    
    public Venda getVendaSelecionado() {
        return venSelecionado;
    }
    
    private Venda pegarLinhaSelecionada() {
        venSelecionado = null;
        int linha = tabelaVendas.getSelectedRow();
        if (linha >= 0) {
            venSelecionado = (Venda) tabelaVendas.getValueAt(linha, 0);
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um PEDIDO.");
        }  
        return venSelecionado;
    }
    
    private void excluirLinhaSelecionada() {
        int linha = tabelaVendas.getSelectedRow();
        if (linha >= 0) {
            ((DefaultTableModel) tabelaVendas.getModel()).removeRow(linha);
        } 
    }

