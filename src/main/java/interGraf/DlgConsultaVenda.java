    private void botaoBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoBuscarActionPerformed
        
        try {
            List<Venda> lista = gerIg.getGerDominio().pesquisarVenda(cmbTipo.getSelectedIndex(), txtPesquisa.getText() ) ;
            
            // Limpar a tabela
            ((DefaultTableModel) tabelaVendas.getModel()).setRowCount(0);
            
            if ( lista.isEmpty() ) {
                JOptionPane.showMessageDialog(this, "Não existem registros com essa pesquisa.");
            } else {
                for ( Venda ven : lista ){
                    ((DefaultTableModel) tabelaVendas.getModel()).addRow( ven.toArray() );
                }
            }
            
        } catch ( NumberFormatException | ParseException ex) {
            //JOptionPane.showMessageDialog(this, "Favor informar um ID válido. Somente números.");            
            JOptionPane.showMessageDialog(this, ex);            
        } catch ( HibernateException ex) {
            JOptionPane.showMessageDialog(this, "Erro ao pesquisar pedido." + ex);
        } 
    }//GEN-LAST:event_botaoBuscarActionPerformed

